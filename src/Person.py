class Person:

    def __init__(self, name, surname, age, gender, identification_number, size, weight, hair_color, eye_color,
                 marital_status, date_of_birth):
        self.name = name  # ad
        self.surname = surname  # soyad
        self.age = age  # yaş
        self.gender = gender  # cinsiyet
        self.identification_number = identification_number  # tc kimlik numarası
        self.size = size  # boyu
        self.weight = weight  # kilosu
        self.hair_color = hair_color  # saç rengi
        self.eye_color = eye_color  # göz rengi
        self.maritial_status = marital_status  # medeni hali
        self.date_of_birth = date_of_birth  # soğum tarihi
